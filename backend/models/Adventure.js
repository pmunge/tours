const mongoose = require("mongoose");

const adventureSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  imageUrl: {
    type: String,
  },
});

const Adventure = mongoose.model("Adventure", adventureSchema);

module.exports = Adventure;

